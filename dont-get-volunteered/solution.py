def solution(src, dest):
    src_x, src_y = get_pos(src)
    dest_x, dest_y = get_pos(dest)
    
    diff_x = abs(src_x - dest_x)
    diff_y = abs(src_y - dest_y)
    
    if diff_x > diff_y:
        diff_x, diff_y = diff_y, diff_x
        
    return known_states[diff_x][diff_y]
   

def get_pos(pos):
    return pos % 8, pos // 8
    
